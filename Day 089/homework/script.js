function solution(str) {
  return str.split("").reverse().join("");
}

function main(verb, noun) {
  return verb + noun;
}

const areaOrPerimeter = function (l, w) {
  if (l === w) {
    return l * w;
  } else {
    return 2 * (l + w);
  }
};

function transformNumber(number) {
  if (number > 0) {
    return -number;
  } else {
    return number;
  }
}

function opposite(number) {
  return -num;
}

